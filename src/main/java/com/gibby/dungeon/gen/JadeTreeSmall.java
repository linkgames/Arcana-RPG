    private boolean isRightBlock(final World world, final int i, final int j, final int k) {
        for (int c = 2; c < 9; ++c) {
            if (!world.isAirBlock(i, j + c, k) && world.getBlock(i, j + c, k) != Dungeons.jadeLeaves && world.getBlock(i, j + c, k) != Dungeons.jadeLog) {
                return false;
            }
        }
        return true;
    }

